package lesson_7_Operators;

public class Lesson_1 {

    public static void main(String[] args) {

        // Arithmetic Operators ( + , - , * , / , % , ++ , -- )

        int y = 2 * 4;
        int z = 10 / 2;

        System.out.println(z);

        int i = 2;

        // i = i + 1;
        i++;
        // ++i

        System.out.println(i);

        i--;
        // --i

        System.out.println(i);

        // Assignment Operators

        int a = 10;

        // a = a + 5;
        a += 5;
        a -= 10;

    }

}

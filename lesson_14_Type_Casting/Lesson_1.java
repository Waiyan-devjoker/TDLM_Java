package lesson_14_Type_Casting;

public class Lesson_1 {

    public static void main(String[] args) {

        // Auto Casting
        int i = 20;
        double d = i;

        System.out.println(i);
        System.out.println(d);

        // Manual Casting
        double d1 = 30.45;
        int i1 = (int) d1;

        char c = 'A';

        System.out.println(d1);
        System.out.println(i1);
        System.out.println(c);

    }

}


package lesson_28_Setter_Getter;

public class Todo {

    private String email;
    private String pass;

    public void setEmail(String email){
        this.email = email;
    }

    public String getEmail(){
        return this.email;
    }

    public void setPass(String pass){
        this.pass = pass;
    }

    public String getPass(){
        return this.pass;
    }
    
}
